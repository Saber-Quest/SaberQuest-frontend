/**
 * Enum representing different challenge difficulty levels.
 */
export enum ItemRarity {
  /**
   * Normal difficulty level.
   */
  C = "Common",

  /**
   * Hard difficulty level.
   */
  U = "Uncommon",

  /**
   * Expert difficulty level.
   */
  R = "Rare",

  /**
   * Epic difficulty level.
   */
  E = "Epic",

  /**
   * Legendary difficulty level.
   */
  L = "Legendary",
}
